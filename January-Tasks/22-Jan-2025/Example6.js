/*
Write a JavaScript program to calculate and print the area of a square and rectangle 
and print the output on the console.
*/

let side = 5;

let length = 4;
let breadth = 5;

let areaOfSquare = side * side;

let areaOfRectangle = length * breadth;

console.log(`The area of the square with side ${side} is: ${areaOfSquare}`);
console.log(`The area of the rectangle with length ${length} and breadth ${breadth} is: ${areaOfRectangle}`);

/* output
The area of the square with side 5 is: 25
The area of the rectangle with length 4 and breadth 5 is: 20
*/